# São tipadas dinamicamente pelo interpretador

#exemplos:

nome = "aluno" 
nota = 8.75
inscricao = True
numero = 10

# Função type() retorna o tipo dew dado que a variável guarda

print(type(nome)) # retorna <class 'str'> porque tudo é objetos em py
print(type(nota)) #class float
print(type(inscricao)) #class boleano
print(type(numero)) # class int 
